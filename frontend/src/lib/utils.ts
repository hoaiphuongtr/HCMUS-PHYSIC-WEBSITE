export function toSlug(text: string): string {
  return text
    .split("")
    .map((c) => VIETNAMESE_MAP[c] || c)
    .join("")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

