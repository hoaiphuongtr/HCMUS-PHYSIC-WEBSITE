export function suggestSlugs(name: string): string[] {
  const viSlug = toSlug(name);
  const suggestions = [viSlug];
  const enSlug = SLUG_TRANSLATIONS[viSlug];
  if (enSlug) suggestions.push(enSlug);
  return suggestions;
}
